<?php 

/*
	Las estructuras ciclicas, como su nombre lo indica sirven para repetir instrucciones 
	de codigo un determinado numero de veces o inclusive de manera indeterminada.

	La estructura de ciclo para (for) esta compuesta por 3 partes:
		1. Inicialización
		2. Condición
		3. Incremento o decremento

	Sintaxis:

		for($a = 0; $a < 10; $a++) {
			Este codigo se ejecutara 10 veces
		}

	Ejercicio:
		Hacer un programa que imprima en pantalla 5 veces "hola mundo"
*/

	for($a = 0; $a < 5; $a++) {
		echo "Hola mundo";
	}

?>